function about(){
  var y=10;
  var Y=380;
  var scrollInterval = setInterval(function() {
    if(y>=Y){
      clearInterval(scrollInterval);
      return;
    }
    else{
    y+=10;
    window.scrollBy(0,10);
    }
    }, 0.1);
  
  }
  
